$(document).ready(function() {
    $('.hover-text-one').hide();
    $('.hover-container-one').hide();
    $('.picone').animate({
        opacity:1
        
    });

    $('.picone').hover(function() {
        $(this).stop().animate({opacity:.4},200);
        $('.hover-text-one').fadeIn();
        $('.hover-container-one').fadeIn();
        
    

    }, function() {
        $('.picone').stop().animate({opacity:1},500)
        $('.hover-text-one').fadeOut();
        $('.hover-container-one').fadeOut();

    });
    
    $('.hover-text-one').hover(function() {
        $('.picone').stop().animate({opacity:.4},200);
        $(this).fadeIn();
        $('.hover-container-one').fadeIn();
        
    

    }, function() {
        $('.picone').stop().animate({opacity:1},500)
        $('.hover-text-one').fadeOut();
        $('.hover-container-one').fadeOut();

    });


});


